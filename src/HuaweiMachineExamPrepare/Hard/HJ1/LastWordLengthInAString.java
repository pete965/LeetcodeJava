package HuaweiMachineExamPrepare.Hard.HJ1;

import java.util.Scanner;

public class LastWordLengthInAString {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String input = "";
        while (sc.hasNext()) {
            input = sc.next();
        }
        System.out.println(input.length());
    }
}
